#Crear un programa que pida al usuario ingresar dos números y calcule su suma, resta, multiplicación y división
Numero_1 = eval(input("Ingrese el primer numero: "))
Numero_2 = eval(input("Ingrese el segundo numero: "))
print(f"La suma es: {Numero_1 + Numero_2}, la resta es: {Numero_1 - Numero_2}, la multiplicacion es: {Numero_1 * Numero_2} y la division es: {Numero_1 / Numero_2}")